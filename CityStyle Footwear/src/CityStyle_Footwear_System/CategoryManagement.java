    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (tblCategory.isEditing()) {
        tblCategory.getCellEditor().stopCellEditing();
         }
        int row = tblCategory.getSelectedRow();
        if(row == -1) {
            JOptionPane.showMessageDialog(this, "Please select a category to update!");
            return;
        }

        String id = tblCategory.getValueAt(row, 0).toString(); // ID from column 1
        String newName = tblCategory.getValueAt(row, 1).toString().trim();
        
        if (newName.isEmpty()) {   //if name is not added,stop updating
        JOptionPane.showMessageDialog(this, "Please enter a category name first!"); 
        return;
}

        try {
            Connection con = DatabaseConnection.getConnection();
            String sql = "UPDATE public.category SET categoryname = ? WHERE categoryid = ?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, newName);
            pst.setInt(2, Integer.parseInt(id));

            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Category Updated!");
            loadTable(); // refresh the table
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String name = txtCatName.getText();
    if (name.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter a category name!");
        return;
    }
    try {
        Connection con = DatabaseConnection.getConnection();
        String sql = "INSERT INTO public.category (categoryname) VALUES (?)";
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, name);
        pst.executeUpdate();
        
        JOptionPane.showMessageDialog(this, "Category Added!");
        txtCatName.setText(""); // to clear TextField 
        loadTable(); // refresh the table
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
      int row = tblCategory.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Please select a category to delete!");
        return;
    }
    int id = Integer.parseInt(tblCategory.getValueAt(row, 0).toString());
    
    try {
        Connection con = DatabaseConnection.getConnection();
        String sql ="DELETE FROM public.category WHERE categoryid = ?";
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setInt(1, id);
        pst.executeUpdate();
        
        JOptionPane.showMessageDialog(this, "Category Deleted!");
        loadTable();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

