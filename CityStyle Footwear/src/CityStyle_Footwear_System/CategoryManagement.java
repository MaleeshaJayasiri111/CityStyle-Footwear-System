    private void loadTable() {
        try {
        DefaultTableModel model = (DefaultTableModel) tblCategory.getModel();
        model.setRowCount(0); //clear old data

        Connection con = DatabaseConnection.getConnection();
        String sql = "SELECT categoryid, categoryname FROM public.category ORDER BY categoryid ASC";
        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery(sql);

        while (rs.next()) {
            Vector v = new Vector();
            v.add(rs.getInt("categoryid"));
            v.add(rs.getString("categoryname"));
            model.addRow(v);
        }
    } 
        catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Table Load Error: " + e.getMessage());
    }
         }
