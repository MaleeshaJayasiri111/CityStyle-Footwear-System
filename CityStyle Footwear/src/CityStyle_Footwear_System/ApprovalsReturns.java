    private void btnApproveReturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveReturnActionPerformed
        int row = tblReturns.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Select a record first!");
            return;
        }

        // As security perpose,again request manager password before approve
       String inputPass = JOptionPane.showInputDialog(this, "Manager Authorization Required.\nEnter Password:");
    
       if (inputPass == null || inputPass.isEmpty()) return;

       try {
          Connection con = DatabaseConnection.getConnection();
        
        // Check password correctness, comparering with databse
        String checkSql = "SELECT * FROM public.users WHERE password=? AND userrole='Manager'";
        PreparedStatement pstCheck = con.prepareStatement(checkSql);
        pstCheck.setString(1, inputPass);
        ResultSet rs = pstCheck.executeQuery();

        if (rs.next()) {
            // Only if manager password correct
            int retId = (int) tblReturns.getValueAt(row, 0);
            int shoeId = (int) tblReturns.getValueAt(row, 1);
            int qty = (int) tblReturns.getValueAt(row, 2);

            con.setAutoCommit(false); // Do as a Transaction 

            // Approved the status
            PreparedStatement pst1 = con.prepareStatement("UPDATE public.returns SET status='Approved' WHERE returnid=?");
            pst1.setInt(1, retId);
            pst1.executeUpdate();

            // Increase stock because of a return
            PreparedStatement pst2 = con.prepareStatement("UPDATE public.shoe SET qty = qty + ? WHERE shoeid = ?");
            pst2.setInt(1, qty); 
            pst2.setInt(2, shoeId); 
            pst2.executeUpdate();
            con.commit();
            JOptionPane.showMessageDialog(this, "Return Authorized! Stock has been updated.");
            loadTables(); 
        }
        else {
            JOptionPane.showMessageDialog(this, "Invalid Manager Password!", "Authorization Failed", JOptionPane.ERROR_MESSAGE);
        }
    } 
       catch (Exception e)
       {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_btnApproveReturnActionPerformed

    private void btnApproveDiscountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveDiscountActionPerformed
    int row = tblDiscounts.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Please select a discount request!");
        return;
    }

    // Request manager password
    String inputPass = JOptionPane.showInputDialog(this, "Enter Manager Password to Override Discount:");
    
    if (inputPass == null || inputPass.isEmpty()) return;

    try {
        Connection con = DatabaseConnection.getConnection();
        
        //Check manager details
        String checkSql = "SELECT * FROM public.users WHERE password=? AND userrole='Manager'";
        PreparedStatement pstCheck = con.prepareStatement(checkSql);
        pstCheck.setString(1, inputPass);
        ResultSet rs = pstCheck.executeQuery();

        if (rs.next()) {
            int reqId = Integer.parseInt(tblDiscounts.getValueAt(row, 0).toString());

            // Approve a Discount
            PreparedStatement pst = con.prepareStatement("UPDATE public.discount_requests SET status='Approved' WHERE requestid=?");
            pst.setInt(1, reqId);
            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Discount Request Approved Successfully!");
            table_discount();
        }
        else {
            JOptionPane.showMessageDialog(this, "Invalid Manager Password!", "Access Denied", JOptionPane.ERROR_MESSAGE);
        }
    } 
    catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_btnApproveDiscountActionPerformed

