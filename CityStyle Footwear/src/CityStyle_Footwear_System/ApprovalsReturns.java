    private void loadTables() {
    try {
        Connection con = DatabaseConnection.getConnection();
        
        String sql1 = (userRole.equals("Manager")) ? // load return table 
             "SELECT * FROM public.returns WHERE status='Pending'" :  //Manager will see as pending
             "SELECT * FROM public.returns ORDER BY returnid DESC";
        
        // Load Returns 
        DefaultTableModel model1 = (DefaultTableModel) tblReturns.getModel();
        model1.setRowCount(0);
        ResultSet rs1 = con.createStatement().executeQuery(sql1); 
        while(rs1.next()) {
            model1.addRow(new Object[]{rs1.getInt("returnid"), rs1.getInt("shoeid"), rs1.getInt("qty"), rs1.getString("type"), rs1.getString("reason"), rs1.getString("status")});
        }

        // LOad Discounts 
        DefaultTableModel model2 = (DefaultTableModel) tblDiscounts.getModel();
        model2.setRowCount(0);
        ResultSet rs2 = con.createStatement().executeQuery("SELECT * FROM public.discount_requests WHERE status='Pending'");
        while(rs2.next()) {
            model2.addRow(new Object[]{rs2.getInt("requestid"), rs2.getString("bill_no"), rs2.getBigDecimal("requested_discount"), rs2.getString("status")});
        }
    } catch (Exception e) { JOptionPane.showMessageDialog(this, e.getMessage()); }
}

