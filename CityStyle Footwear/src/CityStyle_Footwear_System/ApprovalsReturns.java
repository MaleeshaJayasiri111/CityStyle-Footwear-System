    private void table_discount() {
    try {
        Connection con = DatabaseConnection.getConnection();
        // get pending requests from discount_requests table
        PreparedStatement pst = con.prepareStatement("SELECT * FROM public.discount_requests ORDER BY requestid DESC");
        ResultSet rs = pst.executeQuery();
        
        DefaultTableModel dtm = (DefaultTableModel)tblDiscounts.getModel(); 
        dtm.setRowCount(0); //clear table
        
        while (rs.next()) {
            Vector v = new Vector();
            v.add(rs.getString("requestid"));
            v.add(rs.getString("bill_no"));
            v.add(rs.getString("requested_discount"));
            v.add(rs.getString("status"));
            dtm.addRow(v);
        }
    } catch (Exception e) {
    }
}
    
    private void btnRequestDiscountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRequestDiscountActionPerformed
       
    String billNo = jTextFieldBillNo.getText();
    String discount = jTextFieldDiscount.getText();
    String status = "Pending"; // begining status as pending

    try {
        Connection con = DatabaseConnection.getConnection();
        // Add data into discount_requests table
        PreparedStatement pst = con.prepareStatement("INSERT INTO public.discount_requests (bill_no, requested_discount, status) VALUES (?, ?, ?)");
        pst.setString(1, billNo);
        pst.setDouble(2, Double.parseDouble(discount));
        pst.setString(3, status);
        pst.executeUpdate();

        JOptionPane.showMessageDialog(this, "Discount Request Sent to Manager!");
        
        // Refresh the table
        table_discount(); 
        
        // clear Fields
        jTextFieldBillNo.setText("");
        jTextFieldDiscount.setText("");
        
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }

    }//GEN-LAST:event_btnRequestDiscountActionPerformed

